package com.raystec.setgetmethod;

 public class Shape {

	private String color = null;
	private int borderWidth = 0;
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getborderWidth() {
		return borderWidth;
	}
	public void setborderWidth(int bw) {
		borderWidth = bw;
	}
}